def addition(num1, num2):
    """add two numbers"""
    return num1 + num2


def subtraction(num1, num2):
    """subtract two numbers"""
    return num1 - num2


def multiplication(num1, num2):
    """Multiply two numbers"""
    return num1 * num2


def division(num1, num2):
    """Divide Two Numbers"""
    return num1 / num2